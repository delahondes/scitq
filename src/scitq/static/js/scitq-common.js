function check_online(func) {
    $.getJSON('/live/ping', function() {
        console.log('online');
        func();
    });
}

var pause_loop = false;
function loop_if_online(func, wait) {
    func();
    var interv = function(w){
        return function(){
            setTimeout(interv, w);
            if (!pause_loop) {
                check_online(func);
            }
            };
    }(wait);

    setTimeout(interv, wait);
}


function pause() { 
    if (!pause_loop) {
        pause_loop = true;
        return true;
    } else {return false;} ;
}
  
function unpause() {
    if (pause_loop) {
        pause_loop = false;
        return true;
    } else 
    {return false;}
}

function pause_page() {
    if (!pause_loop) {
        pause_loop = true;
        document.getElementById('pause').innerHTML='Resume';
    } else {
        pause_loop = false;
        document.getElementById('pause').innerHTML='Pause';
    }
}

function remove_from_array(arr, value){
    for( var i = 0; i < arr.length; i++) { 
        if ( arr[i] === value) { 
            arr.splice(i, 1); 
        }
    }
}

